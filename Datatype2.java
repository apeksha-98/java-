public class Datatype2
{
	public static void main(String[] params)
	{
		byte daysInWeek;
		daysInWeek=7;
		System.out.println(daysInWeek);
		short year;
		year=2020;
		System.out.println(year);
		int speedOfLight;
		speedOfLight=299792458;
		System.out.println(speedOfLight);
		long worldPopulation;
		worldPopulation=780000000;
		System.out.println(worldPopulation);
		float cgpa;
		cgpa=7.53f;
		System.out.println(cgpa);
		double planksConstant;
		planksConstant=6.62607004;
		System.out.println(planksConstant);
		boolean carHasTwoWheels;
		carHasTwoWheels=false;
		System.out.println(carHasTwoWheels);
		char divisionOfClass;
		divisionOfClass='B';
		System.out.println(divisionOfClass);
		String nameOfState;
		nameOfState="Karnataka";
		System.out.println(nameOfState);
	}
} 