public class Datatype1
{
	public static void main(String[] params)
	{
		byte age;
		age=23;
		System.out.println(age);
		short daysInYear;
		daysInYear=365;
		System.out.println(daysInYear);
		int numberOfEngineersInIndia;
		numberOfEngineersInIndia=15000000;
		System.out.println(numberOfEngineersInIndia);
		long populationOfIndia;
		populationOfIndia=1380004385;
		System.out.println(populationOfIndia);
		float percentageOfStudent;
		percentageOfStudent=77.5f;
		System.out.println(percentageOfStudent);
		double chargeOfElectron;
		chargeOfElectron=1.602176662;
		System.out.println(chargeOfElectron);
		boolean earthIsFlat;
		earthIsFlat=false;
		System.out.println(earthIsFlat);
		char grade;
		grade='B';
		System.out.println(grade);
		String name;
		name="apeksha";
		System.out.println(name);
		
	}
}