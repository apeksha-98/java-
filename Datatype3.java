public class Datatype3
{
	public static void main(String[] params)
	{
		byte continents;
		continents=7;
		System.out.println(continents);
		short costOfwatchs;
		costOfwatchs=3000;
		System.out.println(costOfwatchs);
		int distanceOfEarthToMoon;
		distanceOfEarthToMoon=384400;
		System.out.println(distanceOfEarthToMoon);
		long numberOfSpecies;
		numberOfSpecies=870000000;
		System.out.println(numberOfSpecies);
		float height;
		height=5.3f;
		System.out.println(height);
		double valueOfPi;
		valueOfPi=3.141592653589;
		System.out.println(valueOfPi);
		boolean oneIsOdd;
		oneIsOdd=true;
		System.out.println(oneIsOdd);
		char bloodGroup;
		bloodGroup='B';
		System.out.println(bloodGroup);
		String week;
		week="tuesday";
		System.out.println(week);
	}
} 